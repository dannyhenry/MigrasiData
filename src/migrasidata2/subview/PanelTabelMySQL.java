    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        // TODO add your handling code here:
        if (jList1.getSelectedIndex() != -1) {
            String tabel = jList1.getSelectedValue().toString();//ambil isinya
//            ResultSet
            ResultSet rs = db.eksekusiSelect("select * from " + tabel);
            try {
                ResultSetMetaData rsmd = rs.getMetaData();
                listStruktur = di.loadStruktur(rsmd, tabel);
                isiTable = di.loadIsiData(rs, rsmd.getColumnCount());
                listKey = di.loadAllKeys("mysql", tabel);
                refreshTabel();
                setStrukturToTabel();
                setDataToTabel();
                setKeyToTabel();
                a1.sesuaikanKolom(jTable1);
                a1.sesuaikanKolom(jTable2);
                a1.sesuaikanKolom(jTable3);
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jList1ValueChanged


