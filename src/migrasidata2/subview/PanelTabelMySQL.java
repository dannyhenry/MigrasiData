    public void setTableToList() {
        for (int i = 0; i < listTable.size(); i++) {
            model.addElement(listTable.get(i));
        }
    }

    public void refreshTabel() {
        tabmod1.setRowCount(0);
        tabmod1.setColumnCount(0);
        tabmod2.setRowCount(0);
        tabmod2.setColumnCount(0);
        tabmod3.setRowCount(0);
        tabmod3.setColumnCount(0);
    }

    public void setStrukturToTabel() {
        String koma = "";
        tabmod1.setColumnIdentifiers(new Object[]{"TABLE", "AUTO INCREMENT", "COLUMN NAME", "COLUMN TYPE", "COLUMN SIZE", "NULLABLE"});
        for (int i = 0; i < listStruktur.size(); i++) {
            tabmod2.addColumn(listStruktur.get(i).getNamaKolom());
            if (listStruktur.get(i).getKomaKolom() > 0) {
                koma = listStruktur.get(i).getPanjangkolom() + "," + listStruktur.get(i).getKomaKolom();
            } else {
                koma = String.valueOf(listStruktur.get(i).getPanjangkolom());
            }
            tabmod1.insertRow(i, new Object[]{listStruktur.get(i).getNamaKelas(), listStruktur.get(i).isCekIncrements(), listStruktur.get(i).getNamaKolom(), listStruktur.get(i).getTipedata(), koma, listStruktur.get(i).nullAbles()});
        }
    }

    public void setDataToTabel() {
        for (int i = 0; i < isiTable.size(); i++) {
            String isi[] = isiTable.get(i).split(";");
            tabmod2.insertRow(i, isi);
        }
    }

    public void setKeyToTabel() {
        tabmod3.setColumnIdentifiers(new Object[]{"COLUMN NAME", "CONSTRAINT TYPE", "KEY NAME", "COLUMN REFRENCES", "SOURCE TABLE"});
        for (int i = 0; i < listKey.size(); i++) {
            tabmod3.insertRow(i, new Object[]{listKey.get(i).getNamaKolom(), listKey.get(i).getJenisKey(), listKey.get(i).getNamaKey(), listKey.get(i).getAsalKey(), listKey.get(i).getNamaTabel()});
        }
    }

